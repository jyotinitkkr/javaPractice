package predictOutput;

public class CompareStrings {

	public static void main(String[] args) {
	// strings are compared lexicographically.
		System.out.println("apple".compareTo("banana")); // generates -1, difference between a and b is by number -1 (1-2).
		System.out.println("bag".compareTo("banana")); // generates -7, difference between g and n is by number -7 (7-14).

	}

}
